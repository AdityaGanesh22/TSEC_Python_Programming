def add_numbers(a: float, b: float) -> float:
    return a + b

def subtract_numbers(a: float, b: float) -> float:
    return a - b

def multiply_numbers(a: float, b: float) -> float:
    return a * b

def divide_numbers(a: float, b: float) -> float:
    return a / b

def modulus_numbers(a: int, b: int) -> int:
    return a % b